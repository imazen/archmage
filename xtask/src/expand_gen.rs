fn gen_combination_tests(files: &mut Vec<TestFile>) {
    // arcane calling rite
    files.push(TestFile {
        path: "combinations/arcane_calls_rite.rs".to_string(),
        content: "use archmage::{arcane, rite, X64V3Token};\n\
                  #[rite(v3)]\n\
                  fn normalize(v: &[f32; 4]) -> [f32; 4] {\n\
                      let mut s = 0.0f32;\n\
                      for &x in v { s += x * x; }\n\
                      let inv = 1.0 / s.sqrt();\n\
                      [v[0]*inv, v[1]*inv, v[2]*inv, v[3]*inv]\n\
                  }\n\
                  #[arcane]\n\
                  fn process(token: X64V3Token, data: &[f32; 4]) -> [f32; 4] {\n\
                      let _ = token;\n\
                      normalize(data)\n\
                  }\n\
                  fn main() {}\n"
            .to_string(),
    });

    // autoversion chain
    files.push(TestFile {
        path: "combinations/autoversion_chain.rs".to_string(),
        content: "use archmage::autoversion;\n\
                  #[autoversion]\n\
                  fn inner(data: &[f32; 4]) -> f32 { data.iter().sum() }\n\
                  #[autoversion]\n\
                  fn outer(data: &[f32; 4], scale: f32) -> f32 { inner(data) * scale }\n\
                  fn main() {}\n"
            .to_string(),
    });

    // token downgrade
    files.push(TestFile {
        path: "combinations/token_downgrade.rs".to_string(),
        content: "use archmage::{arcane, X64V3Token, X64V4Token};\n\
                  #[arcane]\n\
                  fn v3_helper(_t: X64V3Token, x: f32) -> f32 { x }\n\
                  #[arcane]\n\
                  fn v4_caller(token: X64V4Token, x: f32) -> f32 {\n\
                      v3_helper(token.v3(), x) + 1.0\n\
                  }\n\
                  fn main() {}\n"
            .to_string(),
    });

    // token upgrade conditional
    files.push(TestFile {
        path: "combinations/token_upgrade.rs".to_string(),
        content: "use archmage::{arcane, SimdToken, X64V3Token, X64V4Token};\n\
                  #[arcane]\n\
                  fn v4_fast(_t: X64V4Token, x: f32) -> f32 { x * 4.0 }\n\
                  #[arcane]\n\
                  fn v3_with_upgrade(_t: X64V3Token, x: f32) -> f32 {\n\
                      if let Some(v4) = X64V4Token::summon() { v4_fast(v4, x) }\n\
                      else { x * 2.0 }\n\
                  }\n\
                  fn main() {}\n"
            .to_string(),
    });

    // plain token fn (no macro)
    files.push(TestFile {
        path: "combinations/plain_token_fn.rs".to_string(),
        content: "use archmage::{X64V3Token, SimdToken};\n\
                  fn uses_token(token: X64V3Token, data: &[f32; 4]) -> f32 {\n\
                      let _ = token;\n\
                      data.iter().sum()\n\
                  }\n\
                  fn dispatches() -> Option<f32> {\n\
                      let token = X64V3Token::summon()?;\n\
                      Some(uses_token(token, &[1.0, 2.0, 3.0, 4.0]))\n\
                  }\n\
                  fn main() {}\n"
            .to_string(),
    });
}
