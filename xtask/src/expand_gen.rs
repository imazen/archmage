//! Generate combinatorial macro expansion test files.
//!
//! Produces `tests/expand/{category}/{test_name}.rs` covering every
//! meaningful combination of macro, token position, tier relationship,
//! and function properties.

use std::collections::BTreeMap;
use std::fs;
use std::path::Path;

/// A generated test file.
struct TestFile {
    /// Relative path under tests/expand/
    path: String,
    /// File content
    content: String,
}

pub fn generate_expand_tests(expand_dir: &Path) -> anyhow::Result<()> {
    let mut files = Vec::new();

    gen_arcane_tests(&mut files);
    gen_rite_tests(&mut files);
    gen_autoversion_tests(&mut files);
    gen_incant_tests(&mut files);
    gen_rewrite_tests(&mut files);
    gen_deprecated_tests(&mut files);
    gen_combination_tests(&mut files);

    // Collect existing hand-written files to avoid overwriting
    let generated_marker = "// @generated by xtask expand_gen";

    let mut written = 0;
    let mut categories: BTreeMap<String, usize> = BTreeMap::new();

    for file in &files {
        let full_path = expand_dir.join(&file.path);
        if let Some(parent) = full_path.parent() {
            fs::create_dir_all(parent)?;
        }

        let content = format!("{generated_marker}\n{}", file.content);
        fs::write(&full_path, &content)?;
        written += 1;

        let category = file.path.split('/').next().unwrap_or("root").to_string();
        *categories.entry(category).or_default() += 1;
    }

    println!("Generated {written} expand test files:");
    for (cat, count) in &categories {
        println!("  {cat}/: {count} files");
    }

    Ok(())
}

// ============================================================================
// #[arcane] tests
// ============================================================================

fn gen_arcane_tests(files: &mut Vec<TestFile>) {
    // Expansion modes
    for (mode, mode_attr, self_line, token_param) in [
        ("sibling", "#[arcane]", "", "token: X64V3Token"),
        ("nested", "#[arcane(nested)]", "", "token: X64V3Token"),
        (
            "nested_self",
            "#[arcane(_self = Processor)]",
            "struct Processor { val: f32 }\nimpl Processor {",
            "token: X64V3Token",
        ),
    ] {
        let close = if mode == "nested_self" { "\n}" } else { "" };
        let body = if mode == "nested_self" {
            "_self.val + a"
        } else {
            "a + b"
        };
        let params = if mode == "nested_self" {
            "&self, token: X64V3Token, a: f32"
        } else {
            "token: X64V3Token, a: f32, b: f32"
        };

        files.push(TestFile {
            path: format!("arcane/{mode}.rs"),
            content: format!(
                "use archmage::{{arcane, X64V3Token}};\n\
                 {self_line}\n\
                 {mode_attr}\n\
                 fn process({params}) -> f32 {{ {body} }}\n\
                 {close}\n\
                 fn main() {{}}\n"
            ),
        });
    }

    // Token types
    for (name, token_type, import) in [
        ("token_v2", "X64V2Token", "X64V2Token"),
        ("token_v3", "X64V3Token", "X64V3Token"),
        ("token_v4", "X64V4Token", "X64V4Token"),
        ("token_neon", "NeonToken", "NeonToken"),
        ("token_wasm", "Wasm128Token", "Wasm128Token"),
        ("token_trait_bound", "impl HasX64V2", "HasX64V2"),
        ("token_generic", "T", "HasX64V2"),
    ] {
        let generic = if name == "token_generic" {
            "<T: HasX64V2>"
        } else {
            ""
        };
        files.push(TestFile {
            path: format!("arcane/{name}.rs"),
            content: format!(
                "use archmage::{{arcane, {import}}};\n\
                 #[arcane]\n\
                 fn process{generic}(token: {token_type}, a: f32) -> f32 {{ a + 1.0 }}\n\
                 fn main() {{}}\n"
            ),
        });
    }

    // Token positions
    for (name, params) in [
        ("token_first", "token: X64V3Token, a: f32, b: f32"),
        ("token_middle", "a: f32, token: X64V3Token, b: f32"),
        ("token_last", "a: f32, b: f32, token: X64V3Token"),
    ] {
        files.push(TestFile {
            path: format!("arcane/{name}.rs"),
            content: format!(
                "use archmage::{{arcane, X64V3Token}};\n\
                 #[arcane]\n\
                 fn process({params}) -> f32 {{ let _ = token; a + b }}\n\
                 fn main() {{}}\n"
            ),
        });
    }

    // Unsafe fn
    files.push(TestFile {
        path: "arcane/unsafe_fn.rs".to_string(),
        content: "use archmage::{arcane, X64V3Token};\n\
                  #[arcane]\n\
                  unsafe fn process(token: X64V3Token, ptr: *const f32) -> f32 {\n\
                      unsafe { *ptr }\n\
                  }\n\
                  fn main() {}\n"
            .to_string(),
    });

    // Import intrinsics
    files.push(TestFile {
        path: "arcane/import_intrinsics.rs".to_string(),
        content: "use archmage::{arcane, X64V3Token};\n\
                  #[arcane(import_intrinsics)]\n\
                  fn process(token: X64V3Token, a: &[f32; 8], b: &[f32; 8]) -> [f32; 8] {\n\
                      let va = _mm256_loadu_ps(a);\n\
                      let vb = _mm256_loadu_ps(b);\n\
                      let s = _mm256_add_ps(va, vb);\n\
                      let mut out = [0.0f32; 8];\n\
                      _mm256_storeu_ps(&mut out, s);\n\
                      out\n\
                  }\n\
                  fn main() {}\n"
            .to_string(),
    });

    // Wildcard token
    files.push(TestFile {
        path: "arcane/wildcard_token.rs".to_string(),
        content: "use archmage::{arcane, X64V3Token};\n\
                  #[arcane]\n\
                  fn process(_: X64V3Token, a: f32) -> f32 { a * 2.0 }\n\
                  fn main() {}\n"
            .to_string(),
    });

    // cfg feature
    files.push(TestFile {
        path: "arcane/cfg_feature.rs".to_string(),
        content: "use archmage::{arcane, X64V3Token};\n\
                  #[arcane(cfg(my_feature))]\n\
                  fn process(token: X64V3Token, a: f32) -> f32 { a }\n\
                  fn main() {}\n"
            .to_string(),
    });
}

