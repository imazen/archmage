fn gen_incant_tests(files: &mut Vec<TestFile>) {
    let callee_defs = "\
        use archmage::{arcane, incant, X64V3Token, X64V4Token, NeonToken, ScalarToken};\n\
        #[arcane] fn inner_v4(_t: X64V4Token, x: f32) -> f32 { x * 4.0 }\n\
        #[arcane] fn inner_v3(_t: X64V3Token, x: f32) -> f32 { x * 2.0 }\n\
        #[arcane] fn inner_neon(_t: NeonToken, x: f32) -> f32 { x * 2.0 }\n\
        fn inner_scalar(_t: ScalarToken, x: f32) -> f32 { x }\n";

    // Token prepended (default)
    files.push(TestFile {
        path: "incant/token_prepend.rs".to_string(),
        content: format!(
            "{callee_defs}\
             fn dispatch(x: f32) -> f32 {{ incant!(inner(x), [v3, scalar]) }}\n\
             fn main() {{}}\n"
        ),
    });

    // Explicit Token marker — first
    files.push(TestFile {
        path: "incant/token_explicit_first.rs".to_string(),
        content: format!(
            "{callee_defs}\
             fn dispatch(x: f32) -> f32 {{ incant!(inner(Token, x), [v3, scalar]) }}\n\
             fn main() {{}}\n"
        ),
    });

    // Explicit Token marker — last
    let callee_defs_last = "\
        use archmage::{arcane, incant, X64V3Token, ScalarToken};\n\
        #[arcane] fn inner_v3(x: f32, _t: X64V3Token) -> f32 { x * 2.0 }\n\
        fn inner_scalar(x: f32, _t: ScalarToken) -> f32 { x }\n";

    files.push(TestFile {
        path: "incant/token_explicit_last.rs".to_string(),
        content: format!(
            "{callee_defs_last}\
             fn dispatch(x: f32) -> f32 {{ incant!(inner(x, Token), [v3, scalar]) }}\n\
             fn main() {{}}\n"
        ),
    });

    // Default tiers
    files.push(TestFile {
        path: "incant/default_tiers.rs".to_string(),
        content: format!(
            "{callee_defs}\
             fn dispatch(x: f32) -> f32 {{ incant!(inner(x)) }}\n\
             fn main() {{}}\n"
        ),
    });

    // Tier modifiers
    files.push(TestFile {
        path: "incant/tier_modifiers.rs".to_string(),
        content: format!(
            "{callee_defs}\
             fn dispatch(x: f32) -> f32 {{ incant!(inner(x), [-neon, -wasm128]) }}\n\
             fn main() {{}}\n"
        ),
    });

    // Feature-gated tier
    files.push(TestFile {
        path: "incant/feature_gated.rs".to_string(),
        content: format!(
            "{callee_defs}\
             fn dispatch(x: f32) -> f32 {{ incant!(inner(x), [v3(cfg(avx_opt)), scalar]) }}\n\
             fn main() {{}}\n"
        ),
    });
}

// ============================================================================
// Rewrite tests (incant! inside tier macros)
// ============================================================================

fn gen_rewrite_tests(files: &mut Vec<TestFile>) {
    let callee_defs = "\
        #[arcane] fn inner_v4(_t: X64V4Token, x: f32) -> f32 { x * 4.0 }\n\
        #[arcane] fn inner_v3(_t: X64V3Token, x: f32) -> f32 { x * 2.0 }\n\
        fn inner_scalar(_t: ScalarToken, x: f32) -> f32 { x }\n";

    let callee_defs_last = "\
        #[arcane] fn inner_v3(x: f32, _t: X64V3Token) -> f32 { x * 2.0 }\n\
        fn inner_scalar(x: f32, _t: ScalarToken) -> f32 { x }\n";

    // Caller = arcane × tier relationship
    for (name, token_type, tier_list, desc) in [
        (
            "arcane_exact",
            "X64V3Token",
            "v3, scalar",
            "exact tier match",
        ),
        (
            "arcane_downgrade",
            "X64V4Token",
            "v3, scalar",
            "V4 caller → V3 callee",
        ),
        (
            "arcane_upgrade",
            "X64V3Token",
            "v4, v3, scalar",
            "V3 caller, V4 available",
        ),
        (
            "arcane_upgrade_gated",
            "X64V3Token",
            "v4(cfg(avx512)), v3, scalar",
            "V3 caller, V4 feature-gated",
        ),
    ] {
        files.push(TestFile {
            path: format!("rewrite/{name}.rs"),
            content: format!(
                "// Rewrite: {desc}\n\
                 use archmage::{{arcane, X64V3Token, X64V4Token, ScalarToken}};\n\
                 {callee_defs}\
                 #[arcane]\n\
                 fn outer(token: {token_type}, x: f32) -> f32 {{\n\
                     incant!(inner(x), [{tier_list}])\n\
                 }}\n\
                 fn main() {{}}\n"
            ),
        });
    }

    // Caller = arcane, explicit Token position
    files.push(TestFile {
        path: "rewrite/arcane_token_last.rs".to_string(),
        content: format!(
            "// Rewrite: explicit Token marker, token last in callee\n\
             use archmage::{{arcane, X64V3Token, ScalarToken}};\n\
             {callee_defs_last}\
             #[arcane]\n\
             fn outer(token: X64V3Token, x: f32) -> f32 {{\n\
                 incant!(inner(x, Token), [v3, scalar])\n\
             }}\n\
             fn main() {{}}\n"
        ),
    });

    // Caller = arcane, scalar fallback (cross-arch)
    files.push(TestFile {
        path: "rewrite/scalar_fallback.rs".to_string(),
        content: "// Rewrite: x86 caller, callee only has neon — scalar fallback\n\
                  use archmage::{arcane, X64V3Token, NeonToken, ScalarToken};\n\
                  #[arcane] fn inner_neon(_t: NeonToken, x: f32) -> f32 { x * 2.0 }\n\
                  fn inner_scalar(_t: ScalarToken, x: f32) -> f32 { x }\n\
                  #[arcane]\n\
                  fn outer(token: X64V3Token, x: f32) -> f32 {\n\
                      incant!(inner(x), [neon, scalar])\n\
                  }\n\
                  fn main() {}\n"
            .to_string(),
    });

    // Caller = autoversion × tier relationship
    for (name, tier_list, desc) in [
        ("autoversion_exact", "v3, scalar", "exact match"),
        ("autoversion_upgrade", "v4, v3, scalar", "V4 upgrade"),
        (
            "autoversion_upgrade_gated",
            "v4(cfg(avx512)), v3, scalar",
            "V4 gated upgrade",
        ),
    ] {
        files.push(TestFile {
            path: format!("rewrite/{name}.rs"),
            content: format!(
                "// Rewrite: autoversion caller, {desc}\n\
                 use archmage::{{autoversion, arcane, incant, X64V3Token, X64V4Token, ScalarToken}};\n\
                 #[arcane] fn inner_v4(_t: X64V4Token, x: f32) -> f32 {{ x * 4.0 }}\n\
                 #[arcane] fn inner_v3(_t: X64V3Token, x: f32) -> f32 {{ x * 2.0 }}\n\
                 fn inner_scalar(_t: ScalarToken, x: f32) -> f32 {{ x }}\n\
                 #[autoversion(v3, scalar)]\n\
                 fn outer(x: f32) -> f32 {{\n\
                     incant!(inner(x), [{tier_list}])\n\
                 }}\n\
                 fn main() {{}}\n"
            ),
        });
    }

    // Caller = rite with token
    files.push(TestFile {
        path: "rewrite/rite_exact.rs".to_string(),
        content: "// Rewrite: rite caller, exact match\n\
             use archmage::{rite, arcane, X64V3Token, ScalarToken};\n\
             #[arcane] fn inner_v3(_t: X64V3Token, x: f32) -> f32 { x * 2.0 }\n\
             fn inner_scalar(_t: ScalarToken, x: f32) -> f32 { x }\n\
             #[rite]\n\
             fn outer(token: X64V3Token, x: f32) -> f32 {\n\
                 incant!(inner(x), [v3, scalar])\n\
             }\n\
             fn main() {}\n"
            .to_string(),
    });

    // Caller = rite multi-tier
    files.push(TestFile {
        path: "rewrite/rite_multi.rs".to_string(),
        content: "// Rewrite: rite multi-tier, per-variant rewrite\n\
                  use archmage::{rite, arcane, incant, X64V3Token, NeonToken, ScalarToken};\n\
                  #[arcane] fn inner_v3(_t: X64V3Token, x: f32) -> f32 { x * 2.0 }\n\
                  #[arcane] fn inner_neon(_t: NeonToken, x: f32) -> f32 { x * 2.0 }\n\
                  fn inner_scalar(_t: ScalarToken, x: f32) -> f32 { x }\n\
                  #[rite(v3, neon)]\n\
                  fn outer(x: f32) -> f32 {\n\
                      incant!(inner(x), [v3, neon, scalar])\n\
                  }\n\
                  fn main() {}\n"
            .to_string(),
    });

    // Caller = rite tokenless (should NOT rewrite)
    files.push(TestFile {
        path: "rewrite/rite_tokenless_passthrough.rs".to_string(),
        content: "// Rewrite: tokenless rite — incant! NOT rewritten (no token to pass)\n\
                  use archmage::{rite, incant, arcane, X64V3Token, ScalarToken};\n\
                  #[arcane] fn inner_v3(_t: X64V3Token, x: f32) -> f32 { x * 2.0 }\n\
                  fn inner_scalar(_t: ScalarToken, x: f32) -> f32 { x }\n\
                  #[rite(v3)]\n\
                  fn outer(x: f32) -> f32 {\n\
                      incant!(inner(x), [v3, scalar])\n\
                  }\n\
                  fn main() {}\n"
            .to_string(),
    });
}

